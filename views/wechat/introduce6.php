2.返利说明
业绩：您伞下所有用户消耗的服务费统称为
业绩。

服务费为游戏底注的40%~50%(百人场游戏为每局赢的货币*5%)

收益：每周根据代理的业绩为代理定级，那么收益为：
直属玩家消耗*对应返利比例+直属代理业绩*返利比例差
    <pre>
3.收益模拟

例如您下级有2个代理，5个直属玩家；
代理甲每周给您贡献20万元业绩
代理乙每周给您贡献6万元业绩
5个直属玩家每周给您贡献15万元业绩
您的总业绩等于：
下级代理甲业绩20万元 +下级代理乙业绩6万元+ 玩家业绩15万元=41万元
您的代理等级就是V7（享受70%返利比例）
下级代理甲是V5等级（享受50%返利比例）
下级代理乙是V4等级（享受45%返利比例）

您的总收益=A（跟下级代理的返利差值）+B（下级玩家的返利）

A部分收益等于：
“您的返利比例 减去 每个下级代理返利比例”
A甲代理= 20万 X（70%-50%）= 4万元
A乙代理= 6万 X （70%-45%）=1.5万元
A=A甲代理4万元+A乙代理1.5万元=5.5万元

B部分收益等于：
玩家业绩的15万元 X 70%=10.5万元

您的总收益就是5.5万元+10.5万元=16万元

